# Initialize a list

my_list = list(range(0, 10))
print(my_list)



# Add value at the end of the list

my_list.append(10)
print(my_list)



# Insert value in a particular index

my_list.insert(0, 11)
print(my_list)


# Remove last element

my_list.pop()
print(my_list)


# Remove particular value

my_list.remove(11)
print(my_list)





